
// Método 1: Sumar dos números
export function sum(a, b) {
    return a + b;
  }
  
  // Método 2: Verificar si un número es par
  export function isPar(number) {
    return number % 2 === 0;
  }
  
  // Método 3: Verificar si un número es impar
  export function isImpar(number) {
    return number % 2 !== 0;
  }
  